#!/usr/bin/env node
/**
 * Reads shared/scene_ops.json and generates web/src/generated/sceneOps.ts
 * containing typed op definitions and a namespace interface for the Proxy API.
 */
import { readFileSync, writeFileSync, mkdirSync } from "fs";
import { dirname, resolve } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const manifestPath = resolve(__dirname, "../shared/scene_ops.json");
const outPath = resolve(__dirname, "../web/src/generated/sceneOps.ts");

const ops = JSON.parse(readFileSync(manifestPath, "utf-8"));

const tsTypeMap = {
  string: "string",
  number: "number",
  boolean: "boolean",
  any: "unknown",
};

function tsType(t) {
  return tsTypeMap[t] ?? "unknown";
}

function returnTsType(op) {
  if (!op.returns) return "void";
  return tsType(op.returns);
}

let out = "// AUTO-GENERATED by scripts/gen-scene-ops.mjs — do not edit\n\n";

// --- SceneOpDef: array of op metadata for runtime use ---
out += "export type SceneOpDef = {\n";
out += "  op: string;\n";
out += "  args: { name: string; type: string }[];\n";
out += "  returns?: string;\n";
out += "};\n\n";

out += "export const sceneOpDefs: SceneOpDef[] = ";
out += JSON.stringify(ops, null, 2);
out += ";\n\n";

// --- Per-op discriminated union ---
out += "// Discriminated union of all scene ops\n";
for (const op of ops) {
  const tag = op.op.replace(/\./g, "_");
  const argTypes = op.args.map((a) => `${a.name}: ${tsType(a.type)}`).join("; ");
  out += `export type SceneOp_${tag} = { op: "${op.op}"; args: [${op.args.map((a) => tsType(a.type)).join(", ")}] };\n`;
}
out += "\nexport type SceneOp =\n";
out += ops.map((op) => `  | SceneOp_${op.op.replace(/\./g, "_")}`).join("\n");
out += ";\n\n";

// --- Result type ---
out += "export type SceneOpResult = { ok: boolean; error?: string; result?: unknown };\n\n";

// --- Namespace interface tree ---
// Group ops by first segment (e.g. "runtime")
const groups = {};
for (const op of ops) {
  const [ns, method] = op.op.split(".");
  if (!groups[ns]) groups[ns] = [];
  groups[ns].push({ method, op });
}

for (const [ns, methods] of Object.entries(groups)) {
  const iface = `Engine${ns[0].toUpperCase()}${ns.slice(1)}Api`;
  out += `export interface ${iface} {\n`;
  for (const { method, op } of methods) {
    const params = op.args.map((a) => `${a.name}: ${tsType(a.type)}`).join(", ");
    const ret = returnTsType(op);
    out += `  ${method}(${params}): Promise<${ret}>;\n`;
  }
  out += "}\n\n";
}

out += "export interface EngineApi {\n";
for (const ns of Object.keys(groups)) {
  const iface = `Engine${ns[0].toUpperCase()}${ns.slice(1)}Api`;
  out += `  ${ns}: ${iface};\n`;
}
out += "}\n";

mkdirSync(dirname(outPath), { recursive: true });
writeFileSync(outPath, out, "utf-8");
console.log(`gen-scene-ops: wrote ${outPath}`);
